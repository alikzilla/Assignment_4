console.log(1 < 2 < 3);
console.log(3 > 2 > 1);

/*
console.log(1 < 2 < 3); compares whether 1 is less than 2, which is true, 
and then whether true is less than 3, which is also true. So, the output is true.

console.log(3 > 2 > 1); compares whether 3 is greater than 2, which is true, 
and then whether true is greater than 1, which is false. So, the output is false.
*/